"""
answer_keys.py — Per-character answer keys for the quiz

Usage:
    python answer_keys.py <franchise_dir>

For each character in characters.json, constructs a *profile-reproducing* set of quiz
answers: option values chosen so the scored OCEAN profile reproduces that character's own
raw profile (snapped to the achievable grid). Each key is then run through the exact same
scoring/matching math as the runtime (imported from validate.py) and asserted to match the
character itself.

This is the concrete proof that every character is reachable — there is at least one set of
answers that maps to each — and doubles as a cheat-sheet / test fixture.

Writes:
    out/answer_keys.json   machine-readable: {name, answers{qid:value}, predicted_match, raws, distance}
    out/answer_keys.md     human cheat-sheet: chosen option label per question, grouped by trait

Makes ZERO API calls.
"""

import json
import sys
from pathlib import Path

# Reuse the *exact* scoring math the runtime mirrors (no second copy to drift).
sys.path.insert(0, str(Path(__file__).parent))
from validate import answers_to_raw, raw_to_z, find_match, euclidean, TRAITS


def contributions_for_sum(total: int, n: int = 5) -> list[int]:
    """Split `total` (in [n, 5n]) across n items, each in [1,5], as evenly as possible."""
    total = max(n, min(5 * n, total))
    base, rem = divmod(total, n)
    return [base + 1] * rem + [base] * (n - rem)


def build_answers_for_character(character: dict, questions_by_trait: dict[str, list[dict]]) -> dict[str, int]:
    """Pick option values reproducing the character's raw OCEAN profile."""
    answers: dict[str, int] = {}
    for trait, questions in questions_by_trait.items():
        target_raw = character["raw"][trait]
        # achievable contribution sum (reverse-adjusted values) for this trait
        total = round(target_raw / 5) + 5            # raw 0->5, 100->25
        contribs = contributions_for_sum(total, len(questions))
        for q, k in zip(questions, contribs):
            # convert reverse-adjusted contribution k back to the actual option value
            value = (6 - k) if q.get("reverse") else k
            answers[q["id"]] = value
    return answers


def run(franchise_dir: Path) -> None:
    characters_path = franchise_dir / "out" / "characters.json"
    quiz_path = franchise_dir / "out" / "quiz.json"

    if not characters_path.exists():
        print("ERROR: out/characters.json not found. Run build_profiles.py first.")
        sys.exit(1)
    if not quiz_path.exists():
        print("ERROR: out/quiz.json not found. Author the quiz first.")
        sys.exit(1)

    char_data = json.loads(characters_path.read_text(encoding="utf-8"))
    quiz_data = json.loads(quiz_path.read_text(encoding="utf-8"))
    characters = char_data["characters"]
    stats = char_data["stats"]
    questions = quiz_data["questions"]
    franchise = char_data.get("franchise", franchise_dir.name)

    # group questions by trait, preserving quiz order
    questions_by_trait: dict[str, list[dict]] = {t: [] for t in TRAITS}
    for q in questions:
        questions_by_trait[q["trait"]].append(q)

    # option-value -> label lookup, per question, for the human cheat-sheet
    label_of: dict[str, dict[int, str]] = {
        q["id"]: {o["value"]: o["label"] for o in q["options"]} for q in questions
    }

    keys = []
    failures = []
    seen_vectors = {}

    for character in characters:
        name = character["name"]
        answers = build_answers_for_character(character, questions_by_trait)

        raw = answers_to_raw(answers, questions)
        z = raw_to_z(raw, stats)
        match = find_match(z, characters)
        distance = euclidean(z, next(c["z"] for c in characters if c["name"] == name))

        if match != name:
            failures.append((name, match))

        vec = tuple(answers[q["id"]] for q in questions)
        if vec in seen_vectors:
            failures.append((name, f"DUPLICATE answer vector with {seen_vectors[vec]}"))
        seen_vectors[vec] = name

        keys.append({
            "name": name,
            "answers": answers,
            "predicted_match": match,
            "target_raw": {t: round(character["raw"][t], 1) for t in TRAITS},
            "achieved_raw": {t: round(raw[t], 1) for t in TRAITS},
            "distance": round(distance, 4),
        })

    # ---- write machine-readable artifact ----
    out_json = franchise_dir / "out" / "answer_keys.json"
    out_json.write_text(
        json.dumps({"franchise": franchise, "keys": keys}, indent=2, ensure_ascii=False),
        encoding="utf-8",
    )

    # ---- write human cheat-sheet ----
    lines = [f"# {franchise} — per-character answer keys\n",
             "Profile-reproducing answer keys. Answering a quiz exactly as listed below matches that "
             "character. Generated by `build/core/answer_keys.py`; verified against the runtime scoring "
             "math.\n"]
    for key in keys:
        lines.append(f"\n## {key['name']}\n")
        for trait in TRAITS:
            lines.append(f"\n**{trait.capitalize()}**\n")
            for q in questions_by_trait[trait]:
                v = key["answers"][q["id"]]
                lines.append(f"- {q['id']} (value {v}): {label_of[q['id']][v]}")
        lines.append("")
    out_md = franchise_dir / "out" / "answer_keys.md"
    out_md.write_text("\n".join(lines), encoding="utf-8")

    verified = len(keys) - len(failures)
    print(f"  Wrote {out_json.relative_to(franchise_dir.parent.parent)}")
    print(f"  Wrote {out_md.relative_to(franchise_dir.parent.parent)}")
    print(f"\n{verified}/{len(keys)} keys verified, {len(failures)} failures")
    if failures:
        for name, got in failures:
            print(f"  ✗ {name} -> {got}")
        sys.exit(1)


if __name__ == "__main__":
    if len(sys.argv) < 2:
        print("Usage: python answer_keys.py <franchise_dir>")
        sys.exit(1)
    run(Path(sys.argv[1]))
